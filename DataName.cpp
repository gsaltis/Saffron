/*****************************************************************************!
 * Global Headers
 *****************************************************************************/
#include <QtCore>
#include <QtGui>
#include <QWidget>

/*****************************************************************************!
 * Local Headers
 *****************************************************************************/
#include "DataName.h"

/*****************************************************************************!
 * Function : DataName
 *****************************************************************************/
DataName::DataName
() : QWidget()
{
  name = QString();
}

/*****************************************************************************!
 * Function : DataName
 *****************************************************************************/
DataName::DataName
(QString InName) : QWidget()
{
  name = QString(InName);
}

/*****************************************************************************!
 * Function : ~DataName
 *****************************************************************************/
DataName::~DataName
()
{
}

/*****************************************************************************!
 * Function : ToString
 *****************************************************************************/
QString
DataName::ToString(void)
{
  return name;
}

/*****************************************************************************!
 * Function : FromString
 *****************************************************************************/
void
DataName::FromString
(QString InName)
{
  name = InName;
}

/*****************************************************************************!
 * Function : IsAnonymous
 *****************************************************************************/
bool
DataName::IsAnonymous(void)
{
  return name.isEmpty();
}
